package com.globallogic.estructurasbasicas_3_1;

public class TiposPrimitivos {

	public static void main(String[] args) {
		
		int myNum = 15;
		char myChar = 'a';
		boolean myBool = true;
		float myFloat = (float) 2436.2456;
		short myShort = 500;
		
		
		System.out.println("El valor de myNum es " + myNum);
		System.out.println("El valor de myChar es " + myChar);
		System.out.println("El valor de myBool es " + myBool);
		System.out.println("El valor de myFloat es " + myFloat);
		System.out.println("El valor de myShort es " + myShort);

	}

}
