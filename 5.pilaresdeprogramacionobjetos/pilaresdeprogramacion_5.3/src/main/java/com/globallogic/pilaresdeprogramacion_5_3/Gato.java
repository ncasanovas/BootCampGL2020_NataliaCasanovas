package com.globallogic.pilaresdeprogramacion_5_3;

public class Gato {

	private String nombre;

	private int patas;

	public String getNombre(){ 
		return nombre;
	}

	public int getPatas(){ 
		return patas;
	}

	public void setNombre(String nuevoNombre){ 
		nombre = nuevoNombre;
	}

	public void setPatas(int numeroPatas){ 
		patas = numeroPatas;
	}

}
